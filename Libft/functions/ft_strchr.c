#include <stdio.h>
#include <string.h>

char	*ft_strchr(const char *s, int c)
{
	char	*str;

	str = (char *)s;
	while (*str != c)
	{
		if (*str == '\0')
		{
			return (NULL);
		}
		str++;
	}
	return (str);
}

int main(void)
{
    int c = 'z';
    printf("%s\n", ft_strchr("bonbats", c));
    printf("%s\n", strchr("bonbats", c));
}
